# Write a Python program to swap two variables using third variable = 10
var1 = 10
var2 = 20

print("Before swapping:")
print("Variable 1:", var1)
print("Variable 2:", var2)

temp = var1
var1 = var2
var2 = temp

print("After swapping:")
print("Variable 1:", var1)
print("Variable 2:", var2)